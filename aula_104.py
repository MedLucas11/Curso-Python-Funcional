# Introdução a programação funcional

# É o processo de construir softwares por meio de composição de funções puras, evitando compartilhamento de estados,
# dados mutáveis e, consequentemente, seus efeitos colaterais

# Ela enfatiza a aplicação de funções em contraste da programação imperativa (que enfatiza a mudança de estado do
# programa)

# As linguagens de programação PURAMENTE funcionais são utilizadas mais academicamente do que comercialmente
# Exemplos: Prolog, Lisp, Scheme, Haskel, entre outras

# A programação funcional tem sua origem na matemática formal

# Os princípios de programação funcional implementados em python são:
# - Funções de Primeira Classe
# - Funções de Ordem Superior
# - Funções Anônimas (lambda)
# - Closure
# - Recursão
# - Imutabilidade
# - Lazy Evaluation

