# Funções len(), abs(), sum() e round()

# len() - retorna o tamanho de um iterável
# abs() - retorna o valor absoluto de um elemento
# sum() - somatória de todos os elementos de um iterável, podendo escolher um valor inicial
# round() - arredonda o valor de um elemento, podendo escolher o tanto de casas decimais

print(len('pyPRO - Seja um profissional Python'))

print(abs(-21))

print(sum([1, 4, 5, 12, 25, 41]))
print(sum([1, 4, 5, 12, 25, 41], 100))

print(round(5.23148))
print(round(5.23148, 2))
